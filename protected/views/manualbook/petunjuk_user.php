<b>User</b>
<ul>
    <li>Menu User digunakan untuk melihat daftar akses user yang bisa menggunakan sistem  </li>
</ul>
<b>Langkah-langkah menggunakan menu user :</b>
<ul>
    <li><b>Tambah User</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-user-plus"></i> Tambah User</span> </li>
        <li>Isikan Username </li>
        <li>Isikan Password </li>
        <li>Isikan Konfirmasi Password </li>
        <li>Tekan tombol "<b>simpan user</b>" </li>
    </ul>
    <li><b>Edit User / Reset Password</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-edit"></i> </span> , pada daftar user </li>
        <li>Isikan Username, jika akan di ganti  </li>
        <li>Isikan Password </li>
        <li>Isikan Konfirmasi Password </li>
        <li>Tekan tombol "<b>update</b>" </li>
    </ul>
    <li><b>Hapus User</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-trash"></i> </span> , pada daftar user </li>
        <li>Tekan tombol "<b>OK</b>" ,  untuk melanjutkan menghapus </li>
    </ul>
</ul>