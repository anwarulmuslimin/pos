<b>Kategori</b>
<ul>
    <li>Menu Kategori digunakan untuk mengelola berdasarkan kelompok item atau barang.</li>
</ul>
<b>Langkah-langkah menggunakan menu kategori :</b>
<ul>
    <li><b>Pencarian Kategori</b></li>
    <ul>
        <li>Ketikan nama kategori yang ingin dicari </li>
        <li>Tekan tombol <b><span class="badge"><i class="fa fa-search"></i> Cari</span></b></li>
    </ul>
    <li><b>Menambah Kategori</b></li>
    <ul>
        <li>Tekan tombol "<b>Tambah Kategori</b>"</li>
        <li>Isikan nama kategori pada kolom kategori</li>
        <li>Tekan tombol "<b>simpan</b>"</li>
    </ul>
    <li><b>Mengedit Kategori</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-edit"></i></span></li>
        <li>Edit nama Kategori</li>
        <li>Tekan tombol <span class="badge"><i class="fa fa-check"></i> <b>Simpan</b></span></li>
    </ul>
    <li><b>Menghapus Kategori</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-trash"></i></span>, akan muncul konfirmasi hapus</li>
        <li>Tekan tombol "<b>OK</b>"</li>
    </ul>
    <li><b>Melihat Daftar Item atau Barang</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-list"></i></span></li>
    </ul>
</ul>
