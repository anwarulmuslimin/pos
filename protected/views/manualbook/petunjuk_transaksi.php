<b>Transaksi</b>
<ul>
    <li>Menu Transaksi digunakan untuk melakukan transaksi jual beli dari item atau barang yang sudah di input di sistem</li>
</ul>
<b>Langkah-langkah menggunakan menu Transaksi :</b>
<ul>
    <li><b>Scan Barcode Item</b></li>
    <ul>
        <li> Pastikan kursor telah berada pada kolom input barcode</li>
        <li> Scan barcode pada item </li>
        <li> Isikan jumlah item </li>
        <li>Tekan tombol Enter (pada keyboard) atau <span class="badge"><i class='fa fa-cart-arrow-down'></i> Belanja </span></li>
    </ul>
    <li><b>Pencarian Item</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class='fa fa-search'></i> pencarian </span></li>
        <li>Ketikan nama item yang ingin dicari / langsung tekan tombol <span class="badge"><i class='fa fa-search'></i> cari </span> </li>
        <li>Tekan tombol <span class="badge"><i class='fa fa-check'></i> </span> pada item yang dipilih</li>
    </ul>
    <li><b>Pembayaran</b></li>
    <ul>
        <li>Pilih jenis pembayaran yang akan digunakan</li>
        <li>Tekan tombol <span class="badge"><i class="fa fa-money"></i> <b>Simpan</b></span></li>
    </ul>
    <li><b>Batal pilih item</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-remove"></i> </span> disamping kiri nama item yang sudah dipilih</li>
    </ul>
</ul>
