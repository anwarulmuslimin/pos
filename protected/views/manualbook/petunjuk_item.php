<b>Item</b>
<ul>
    <li>Menu Item digunakan untuk mengelola daftar Item atau Barang .</li>
</ul>
<b>Langkah-langkah menggunakan menu Item :</b>
<ul>
    <li><b>Pencarian Item</b></li>
    <ul>
        <li>Pilih Kategori Item yang akan dicari </li>
        <li>Ketikan nama item yang ingin dicari pada kolom pencarian </li>
        <li>Tekan tombol <b><span class="badge"><i class="fa fa-search"></i> Cari</span></b></li>
    </ul>
    <li><b>Menambahkan Item</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-plus"></i><b> Tambah item</b></span></li>
        <li>Masukkan "<i>Barcode Item</i>",</li>
        <li>Pilih "<i>Kategori Item</i>",</li>
        <li>Masukan "<i>Nama Item</i>",</li>
        <li>Masukan "<i>Jumlah Item</i>",</li>
        <li>Masukan "<i>Harga Beli Item</i>",</li>
        <li>Masukan "<i>Harga Jual Item</i>",</li>
        <li>Tekan tombol <span class="badge"> <b>Simpan</b></span></li>
    </ul>
    <li><b>Ekspor dan Impor Item</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-list"></i> Kelola</span></li>
        <li>Pilih dan Tekan <b> "Ekspor Item" </b>, untuk mendownload daftar item format *.xls </li>
        <li>Pilih dan Tekan <b> "Impor Item" </b>, untuk mengupload daftar item dari format *.xls
            (format excel seperti gambar diatas tombol <span class="badge"> Browse</span>) </li>
            <ul>
                <li>Tekan tombol <span class="badge"><b>Browse</b></span>,Pilih file yang akan diupload </li>
                <li>Tekan tombol <span class="badge"><b> Upload</b></span></li>
            </ul> 
    </ul>
    <li><b>Mengubah / mengedit Item</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-edit"></i> </span></li>
        <li>Edit data Item </li>
        <li>Tekan tombol <span class="badge"><b>Simpan</b></span></li>
    </ul>
    <li><b>Menghapus daftar item</b></li>
    <ul>
        <li>Tekan tombol <span class="badge"><i class="fa fa-trash"></i> </span></li>
        <li>Tekan tombol "<b>OK</b>"</li>
    </ul>
</ul>
